            <p>
                Επιμένουμε η ασκηση να παραμείνει <strong>απλή</strong>:
            </p>
            <ul>
                <li>Δεν χρειάζεται να υπάρχουν χρήστες</li>
                <li>Τα αρχεία δεν χρειάζεται να αποθηκεύονται σε κάποια βάση δεδομένων. Απλώς στον φάκελο.</li>
                <li>Ο καθένας μπορεί να κατεβάσει τα αρχεία από οποιονδήποτε</li>
                <li>Δεν χρειάζονται πολλές σελίδες. Μία σελίδα με μία λίστα από ανεβασμένα αρχεία και μία φόρμα είναι αρκετά.</li>
            </ul>
            <p>
                Επειδή ο browser δεν τρέχει PHP, αλλά αυτή τρέχει στον HTTP server, θα πρέπει να έχετε ένα μηχάνημα
                server όπου θα δοκιμάσετε τον κώδικά σας. Μπορείτε να κάνετε τον υπολογιστή σας server με εγκατάσταση
                του Apache και της PHP.
            </p>
            <p>
                <strong><a href="resources/apachephp/setup.html">Οδηγίες εγκατάστασης Apache και PHP</a></strong>
            </p>
