                η προεπισκόπηση θα πρέπει να δουλεύει σε εικόνες και αρχεία κειμένου. Στη συνέχεια, αν επιθυμεί,
                θα πρέπει να δίνεται η δυνατότητα κανονικής λήψης του αρχείου. Η δυνατότητα αυτή θα πρέπει να
                δίνεται και στα αρχεία άλλων ειδών για τα οποία η προεπισκόπηση δεν είναι δυνατή.
            </p>
            <p>
                Πατώντας κανείς πάνω στο όνομα ενός αρχείου, θα πρέπει να βλέπει την προεπισκόπηση, που για κείμενα
                θα περιλαμβάνει το ίδιο το κείμενο, ενώ για εικόνες την ίδια την εικόνα, καθώς και ένα σύνδεσμο
                για πλήρη λήψη του αρχείου. Πατώντας ξανά, θα πρέπει να μπορεί να κρύβει την προεπισκόπηση. Στα
                αρχεία όπου η προεπισκόπηση δεν είναι εφικτή, θα πρέπει το ένα κλικ να οδηγεί σε κανονική λήψη 
                του αρχείου.
            </p>
            <p>
                Απαιτείται το περιεχόμενο από τα αρχεία κειμένου των οποίων γίνεται η προεπισκόπηση να 
                <strong>μην</strong> περιλαμβάνονται στην ίδια τη σελίδα, αλλά να κατεβαίνει με την χρήση 
